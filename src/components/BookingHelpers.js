export const formatTime = (time) => {
  let formatedTimeArray = []
  formatedTimeArray = time.split(':').map((item) => parseInt(item, 10))
  return formatedTimeArray
}

export const findGymInfo = (gymId, gymData) => {
  let gymInfo
  gymData.forEach(gym => {
    if (gym._id === gymId) {
      gymInfo = gym
    }
  })
  return gymInfo
}